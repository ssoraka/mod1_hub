#include "libft.h"

void	ft_mem_arr_free(void ***arr)
{
	int		n;

	n = 0;
	if (arr == 0 || *arr == 0)
		return ;
	while ((*arr)[n])
	{
		free((*arr)[n]);
		n++;
	}
	free(*arr);
	*arr = NULL;
}
